// Rules pembuatan variable
// 1. ridak boleh diawali angka
// 2. hanya boleh diawali dengan huruf atau .. (garis bawah)
// 3. tidak boleh diawali oleh spasi
// 4. case sensitive
// 5. biasanya camelCase (naming convention)

// deklarasi variabel
// var : bisa di deklarasikan berkali kali meskipun nama variabelnya sama
// let : hanya bisa dideklarasikan sekali, nilainya dapat diubah
// cons : hanya bisa dideklarasikan dan initialisasi sekali dan harus bersamaan

// var jarang dipakai
var nama = "ola";
console.log(nama);
nama = "ulala";
console.log(nama);

//let
let person = "Farrel";
console.log(person);
person = "zidane";

//const
const CONSTANT = "123";
console.log(CONSTANT);
